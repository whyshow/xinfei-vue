          &nbsp;&nbsp;&nbsp;&nbsp;display:&nbsp;grid;
          &nbsp;&nbsp;&nbsp;&nbsp;grid-template-columns:&nbsp;1fr&nbsp;1fr&nbsp;1fr;
          &nbsp;&nbsp;&nbsp;&nbsp;grid-template-rows:&nbsp;50px&nbsp;50px;
          }结果是栅格布局将会把整个宽度分成三个 fraction，每列占据一个 fraction 单位，效果如下：如果我们将grid-template-columns的值更改为1fr 2fr 1fr，第二列的宽度将会是其它两列的两倍。总宽现在是四个 fraction 单位，第二列占据两个 fraction 单位，其它列各占一个 fraction。效果如下：总的来说，fraction 单位值将使你可以很容易的更改列的宽度。高级响应然而，上面列子并没有给出我们想要的响应性，因为网格总是三列宽。我们希望网格能根据容器的宽度改变列的数量。要做到这一点，你必须学习如下三个概念：repeat()首先我们学习repeat()函数。这是一个强大的指定列和行的方法。让我们使用repeat()函数来更改网格:.container&nbsp;{
          &nbsp;&nbsp;&nbsp;&nbsp;display:&nbsp;grid;
          &nbsp;&nbsp;&nbsp;&nbsp;grid-template-columns:&nbsp;repeat(3,&nbsp;100px);
          &nbsp;&nbsp;&nbsp;&nbsp;grid-template-rows:&nbsp;repeat(2,&nbsp;50px);
          }在上面代码中，repeat(3, 100px)等于100px 100px 100px。第一个参数指定行与列的数量，第二个参数指定它们的宽度，因此它将为我们提供与开始时完全相同的布局：auto-fit然后是auto-fit。让我们跳过固定数量的列，将3替换为自适应数量：.container&nbsp;{
          &nbsp;&nbsp;&nbsp;&nbsp;display:&nbsp;grid;
          &nbsp;&nbsp;&nbsp;&nbsp;grid-gap:&nbsp;5px;
          &nbsp;&nbsp;&nbsp;&nbsp;grid-template-columns:&nbsp;repeat(auto-fit,&nbsp;100px);
          &nbsp;&nbsp;&nbsp;&nbsp;grid-template-rows:&nbsp;repeat(2,&nbsp;100px);
          }效果如下:现在，栅格将会根据容器的宽度调整其数量。它会尝试在容器中容纳尽可能多的 100px 宽的列。但如果我们将所有列硬写为 100px，我们将永远没法获得所需的弹性，因为它们很难填充整个宽度。正如你在上图看到的，网格通常在右侧留有空白。minmax()为了解决上述问题，我们需要minmax()。我们将 100px 替换为 minmax(100px, 1fr)，代码如下：.container&nbsp;{
          &nbsp;&nbsp;&nbsp;&nbsp;display:&nbsp;grid;
          &nbsp;&nbsp;&nbsp;&nbsp;grid-gap:&nbsp;5px;
          &nbsp;&nbsp;&nbsp;&nbsp;grid-template-columns:&nbsp;repeat(auto-fit,&nbsp;minmax(100px,&nbsp;1fr));
          &nbsp;&nbsp;&nbsp;&nbsp;grid-template-rows:&nbsp;repeat(2,&nbsp;100px);
          }请注意，所有响应都发生在一行 css 代码中效果如下：正如你所见，效果完美。minmax()函数定义的范围大于或等于 min， 小于或等于 max。因此，现在每列将至少为 100px。但如果有更多的可用空间，栅格布局将简单地将其均分给每列，因为这些列变成了 fraction 单位，而不是 100px。添加图片最后一步是添加图片。这与 CSS Grid 布局无关，但让我们看下代码。我们在每个网格中添加一个图片标签：&lt;div&gt;&lt;img&nbsp;src=&quot;img/forest.jpg&quot;/&gt;&lt;/div&gt;为了使图片适应于每个条目，我们将其宽、高设置为与条目本身一样，我们使用object-fit:cover。这将使图片覆盖它的整个容器，根据需要，浏览器将会对其进行裁剪。.container&nbsp;&gt;&nbsp;div&nbsp;&gt;&nbsp;img&nbsp;{
          &nbsp;&nbsp;&nbsp;&nbsp;width:&nbsp;100%;
          &nbsp;&nbsp;&nbsp;&nbsp;height:&nbsp;100%;
          &nbsp;&nbsp;&nbsp;&nbsp;object-fit:&nbsp;cover;
